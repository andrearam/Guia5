    private void txtcpuKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcpuKeyTyped
        char s = evt.getKeyChar();
        if (txtcpu.getText().length()==2) {
            evt.consume();
        }
      // Verificar si la tecla pulsada no es un digito
      if(!Character.isDigit(s)&&s!=KeyEvent.VK_ENTER&&s!=KeyEvent.VK_BACK_SPACE)
      {
         evt.consume();  // ignorar el evento de teclado
      }
    }//GEN-LAST:event_txtcpuKeyTyped

    private void txtpantallaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpantallaKeyTyped
        char s = evt.getKeyChar();
        int p=(int) evt.getKeyChar();
        if (txtprecio.getText().length()==3) {
            evt.consume();
        }
        if (!Character.isDigit(s) && s != KeyEvent.VK_PERIOD && s!=KeyEvent.VK_BACK_SPACE && s!= KeyEvent.VK_ENTER) {
            getToolkit().beep();
            evt.consume();
        
        }else{
            if (p==46) {
                String cadena=txtpantalla.getText();
                int tamanio=cadena.length();
                for (int i = 0; i <= tamanio; i++) {
                    if (cadena.contains(".")) {
                        evt.setKeyChar((char) KeyEvent.VK_CLEAR);
                        getToolkit().beep();
                        evt.consume();
                    }
                }
            }
        }
    }//GEN-LAST:event_txtpantallaKeyTyped

